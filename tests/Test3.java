package se701;

public class StudentSample {

    public static void main(String[] args) {
        sampleMethod();
    }


    public static void sampleMethod() {
        System.out.println("Execution started");
        for (int j = 0; j < 20; j++) {
            System.out.println(j);
        }

        System.out.println("Execution ended");
    }

}
